# String variables
firstName = "John"
food = "Pizza"
age = 30

print(f"{firstName}")
print(f"{food}")
print(f"{age}")

# Integer variables
year = 2025
month = 8
day = 6

print(f"{year}")
print(f"{month}")
print(f"{day}")

#float variables
price = 19.99
temperature = 36.6
height = 175.5

print(f"{price}")
print(f"{temperature}")
print(f"{height}")
# Boolean variables
isStudent = True
hasLicense = False
isEmployed = True

print(f"{isStudent}")
print(f"{hasLicense}")
print(f"{isEmployed}")
# List variables
fruits = ["Apple", "Banana", "Cherry"]
numbers = [1, 2, 3, 4, 5]
colors = ["Red", "Green", "Blue"]

print(f"{fruits}")
print(f"{numbers}")
print(f"{colors}")
